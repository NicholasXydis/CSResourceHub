def category_count(by_category: dict[str, list[dict]], category: str) -> int:
    return len(by_category.get(category, []))


def shield(label: str, message: str, color: str) -> str:
    return (
        "!["
        f"{label}"
        "](https://img.shields.io/static/v1?"
        f"label={quote_plus(label)}&message={quote_plus(message)}&color={color})"
    )


def ci_badge() -> str:
    badge = f"https://img.shields.io/github/check-runs/{REPO_SLUG}/main?label=ci"
    return f"[![ci]({badge})]({REPO_URL}/actions)"


def last_commit_badge() -> str:
    badge = (
        f"https://img.shields.io/github/last-commit/{REPO_SLUG}"
        "?label=updated&color=0891b2&display_timestamp=author"
    )
    return f"[![updated]({badge})]({REPO_URL}/commits/main)"


def license_badge() -> str:
    badge = (
        "https://img.shields.io/static/v1?"
        "label=license&message=MIT&color=db2777"
    )
    return f"[![license]({badge})]({REPO_URL}/blob/main/LICENSE)"



def count_badge(count: int) -> str:
    return f"({count})"


def typing_svg(lines: list[str]) -> str:
    encoded_lines = ";".join(quote_plus(line) for line in lines)
    return (
        "[![Typing SVG](https://readme-typing-svg.demolab.com?"
        "font=Fira+Code&pause=1000&color=4A90D9&center=true&"
        f"vCenter=true&width=760&lines={encoded_lines})]"
        "(https://git.io/typing-svg)"
    )


def tech_icon(name: str, src: str) -> str:
    return f'<img src="{src}" alt="{name}" width="52" height="52">'


def resource_sort_key(resource: dict):
    resource_type = resource.get("type")
    month = resource.get("month")
    month_order = MONTH_ORDER.get(month, len(MONTH_ORDER) + 1)
    name = resource["name"].lower()
    if resource_type:
        return (0, resource_type, month_order, name)
    return (1, "", month_order, name)


def visible_type(resource: dict) -> str:
    resource_type = resource.get("type", "")
    return "" if resource_type in HIDDEN_README_TYPES else resource_type


def format_type(resource_type: str) -> str:
    label = resource_type.replace("-", " ").replace("_", " ").title()
    return label.replace("Api", "API")


